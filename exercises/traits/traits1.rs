trait AppendBar {
    fn append_bar(self) -> Self;
}

impl AppendBar for String {
    // TODO: Implement `AppendBar` for type `String`.
    // 注意第一个小写self是传入的调用者自身，第二个是返回当前实现的类型,换成String也可以
    fn append_bar(self) -> Self{
        self + "Bar"
    }
}

fn main() {
    let s = String::from("Foo");
    let s = s.append_bar();
    println!("s: {}", s);
}

